/**
 * 
 * In Java, everything is a class. 
 * Classes serve as wrappers for your code
 * but in a true object-oriented sense,
 * they act as "blueprints" for objects
 * 
 */

public class Animal {

    /**
     * 
     * Classes can have properties
     * known as fields. 
     * These are in essence the class' own personal variables 
     * 
     */
    String name; 
    String color;


    /**
     * 
     * whenever a function belongs to class
     * its known as a method 
     * 
     * Since all functions will belong to classes in Java
     * the vast majority of them are referred to as methods 
     * 
     *  */ 
    public void speak() {
        System.out.println("This animal spoke!");
    }

    
}
